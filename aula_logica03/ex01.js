let idade = Number(prompt("Digite a sua idade: "));

alert (idade >= 16 ? "Pode entrar!!" : "Não pode entrar!!");

/*
O operador ternário é uma forma mais concisa de escrever uma estrutura condicional 
if-else. Ele é representado pelo símbolo "?" e ":". já o operador de comparação ">" 
é usado para comparar se a idade é maior ou igual a 16.
No exemplo acima, o operador ternário verifica se a idade é maior ou igual a 16.
Se for verdade, ele retorna "Pode entrar!!", caso contrário, retorna "Não pode entrar!!".
*/